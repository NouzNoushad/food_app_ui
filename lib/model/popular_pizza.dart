class PopularPizza {
  final String image;
  final String title;
  final int calories;
  final double price;
  final List<String> ingredients;
  final double rating;
  final String time;

  PopularPizza({
    required this.image,
    required this.title,
    required this.calories,
    required this.price,
    required this.ingredients,
    required this.rating,
    required this.time,
  });
}
